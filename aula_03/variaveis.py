# Variáveis e Tipos de Dados:

# Em python, você não precisa declarar o tipo de uma variável antes de usá-la. O tipo é inferido automaticamente pelo interpretador.
# Em python a variável é declarada no padrâo snake_case.
    # 1. string ->
    # 2. number - int (número inteiro) ->
    # 3. number -  float (número decimal) ->
    # 4. boolean - True or False ->

nome = "Matheus" # string
email = "matheus@email.com" # string
cidade = "Rio de Janeiro" # string
cpf = 18679659439 # number-int
salario = 1400.65 # Não utiliza vírgula nos números # number-float
casado = False # boolean